package com.teamdev.triangulation;

/**
 * @author : Sergey Pensov
 */
public class TrianglePolygon {
    int x1;
    int y1;
    int x2;
    int y2;
    int x3;
    int y3;

    public TrianglePolygon(int x1, int y1, int x2, int y2, int x3, int y3) {
        this.x1 = x1;
        this.y1 = y1;
        this.x2 = x2;
        this.y2 = y2;
        this.x3 = x3;
        this.y3 = y3;
    }
}
